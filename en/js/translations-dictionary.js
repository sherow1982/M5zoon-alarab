        /**
         * Translate Arabic product title to professional English
         */
        translate(arabicTitle) {
            if (!arabicTitle || typeof arabicTitle !== 'string') {
                return 'Premium Product';
            }
            
            let englishTitle = arabicTitle;
            
            // Apply translations in order of specificity (longest matches first)
            const sortedKeys = Object.keys(this.dictionary).sort((a, b) => b.length - a.length);
            
            sortedKeys.forEach(arabic => {
                if (englishTitle.includes(arabic)) {
                    const regex = new RegExp(arabic.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
                    englishTitle = englishTitle.replace(regex, this.dictionary[arabic]);
                }
            });
            
            // Clean up and format
            englishTitle = this.cleanTitle(englishTitle);
            
            return englishTitle || 'Premium Product';
        },
        
        /**
         * Clean and format translated title
         */
        cleanTitle(title) {
            return title
                // Remove redundant 'Perfume' and 'Watch' prefixes
                .replace(/^(Perfume|Watch)\s+/gi, '')
                
                // Fix brand name spacing and capitalization  
                .replace(/tom\s+ford/gi, 'Tom Ford')
                .replace(/yves\s+saint\s+laurent/gi, 'Yves Saint Laurent')
                .replace(/emporio\s+armani/gi, 'Emporio Armani')
                .replace(/audemars\s+piguet/gi, 'Audemars Piguet')
                .replace(/patek\s+philippe/gi, 'Patek Philippe')
                .replace(/parfums\s+de\s+marly/gi, 'Parfums de Marly')
                
                // Fix measurement formatting
                .replace(/\s*(\d+)\s*ml\s*/gi, ' $1ml')
                .replace(/\s*(\d+)\s*mm\s*/gi, ' $1mm')
                
                // Fix punctuation and spacing
                .replace(/\s*-\s*/g, ' - ')
                .replace(/\s*\+\s*/g, ' + ')
                .replace(/\s*&\s*/g, ' & ')
                .replace(/\s*\*\s*/g, ' & ')
                .replace(/\s*\/\s*/g, ' / ')
                .replace(/\s{2,}/g, ' ')
                
                // Capitalize first letters of important words
                .replace(/\b(premium|luxury|classic|modern|automatic|edition|collection|series|royal|master|professional)\b/gi, 
                    (match) => match.charAt(0).toUpperCase() + match.slice(1).toLowerCase())
                
                // Final cleanup
                .replace(/^\s+|\s+$/g, '')
                .replace(/\s*\.+\s*/g, ' ')
                .replace(/^[\s.-]+|[\s.-]+$/g, '')
                .replace(/\|\|/g, 'II')
                
                // Fix common translation artifacts
                .replace(/\bwatch watch\b/gi, 'Watch')
                .replace(/\bperfume perfume\b/gi, 'Perfume')
                .replace(/\bwith with\b/gi, 'with')
                .replace(/\band and\b/gi, 'and');
        },
        
